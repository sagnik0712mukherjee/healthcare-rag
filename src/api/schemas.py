# ==============================================================================
# src/api/schemas.py
# ==============================================================================
# PURPOSE:
#   Defines all Pydantic models used for API request validation and
#   response serialization. These are the data contracts between the
#   Streamlit frontend and the FastAPI backend.
#
# WHY PYDANTIC SCHEMAS MATTER:
#   FastAPI uses these models to:
#     - Automatically VALIDATE incoming request bodies (wrong type = 422 error)
#     - Automatically SERIALIZE outgoing response data to JSON
#     - Auto-generate OpenAPI (Swagger) documentation at /docs
#
#   Every API route has one request schema (what it accepts) and one
#   response schema (what it returns). This file defines all of them.
#
# NAMING CONVENTION:
#   Request bodies:  <Action>Request   (e.g., QueryRequest, UserCreateRequest)
#   Responses:       <Action>Response  (e.g., QueryResponse, UserResponse)
#   Admin actions:   <Action>Action    (e.g., ReviewAction, TokenResetAction)
#
# USED BY:
#   src/api/routes.py      (all route handlers import these)
#   src/api/main.py        (app startup uses UserCreateRequest for init admin)
# ==============================================================================

from pydantic import BaseModel, EmailStr, Field, field_validator
from typing import Optional
from datetime import datetime


# ==============================================================================
# QUERY SCHEMAS — /query endpoint
# ==============================================================================


class QueryRequest(BaseModel):
    """
    Request body for the main RAG query endpoint (POST /query).

    Fields:
        query (str):
            The user's medical question. Must be between 1 and 2000 characters.
            Example: "What are the early symptoms of type 2 diabetes?"
        session_id (str):
            UUID identifying the user's current chat session.
            Generated by the Streamlit frontend with build_session_id().
            All messages in the same session share short-term memory.
        source_filter (str, optional):
            If provided, restricts retrieval to one source type.
            Valid values: "clinical_case", "image_caption"
            Default: None (retrieve from both sources)
        top_k (int, optional):
            How many FAISS chunks to retrieve. Must be between 1 and 20.
            Default: None (uses settings.retrieval_top_k = 5)
    """

    query: str = Field(
        ...,
        min_length=1,
        max_length=2000,
        description="The user's medical question",
        examples=["What are the early symptoms of type 2 diabetes?"],
    )
    session_id: str = Field(
        ...,
        min_length=36,
        max_length=36,
        description="UUID identifying the current chat session",
    )
    source_filter: Optional[str] = Field(
        default=None,
        description="Restrict retrieval to 'clinical_case' or 'image_caption'",
    )
    top_k: Optional[int] = Field(
        default=None,
        ge=1,
        le=20,
        description="Number of chunks to retrieve (1-20)",
    )

    @field_validator("source_filter")
    @classmethod
    def validate_source_filter(cls, v):
        if v is not None and v not in ("clinical_case", "image_caption"):
            raise ValueError(
                "source_filter must be 'clinical_case', 'image_caption', or null"
            )
        return v


class RetrievedChunkResponse(BaseModel):
    """
    A single retrieved context chunk returned alongside a query response.

    Purpose:
        Included in QueryResponse.retrieved_chunks so the Streamlit frontend
        can display the source evidence panel — showing users exactly which
        clinical cases or image captions backed the AI's answer.
    """

    chunk_text: str
    similarity_score: float
    source: str  # "clinical_case" or "image_caption"
    chunk_id: str
    case_id: Optional[str] = None
    patient_age: Optional[int] = None
    patient_gender: Optional[str] = None
    image_id: Optional[str] = None
    image_type: Optional[str] = None
    labels: Optional[list[str]] = None


class QueryResponse(BaseModel):
    """
    Response body for the main RAG query endpoint (POST /query).

    Fields:
        response_text (str):
            The AI-generated answer. Always includes the medical disclaimer.
        retrieved_chunks (list[RetrievedChunkResponse]):
            The context chunks that were used to generate the response.
            Empty list if the response was served from cache.
        input_tokens (int): Prompt tokens consumed (0 if cached).
        output_tokens (int): Completion tokens consumed (0 if cached).
        total_tokens (int): Total tokens consumed (0 if cached).
        served_from_cache (bool): True if response came from cache.
        was_flagged_for_review (bool): True if flagged for human review.
        risk_score (float): Risk score (0.0 to 1.0) computed by helpers.py.
        success (bool): False if the pipeline encountered a blocking error.
        error (str, optional): Error code if success is False.
    """

    response_text: str
    retrieved_chunks: list[RetrievedChunkResponse] = []
    input_tokens: int = 0
    output_tokens: int = 0
    total_tokens: int = 0
    served_from_cache: bool = False
    was_flagged_for_review: bool = False
    risk_score: float = 0.0
    success: bool = True
    error: Optional[str] = None


